// ===== Handlers =====

const onSelectEmoji = handler<
  CustomEvent<{ value: string; label: string }>,
  { selectedEmoji: Writable<string> }
>((event, { selectedEmoji }) => {
  const { value } = event.detail;
  selectedEmoji.set(value);
});

const clearSelection = handler<unknown, { selectedEmoji: Writable<string> }>(
  (_event, { selectedEmoji }) => {
    selectedEmoji.set("");
  },
);

// ===== The Pattern =====
export const EmojiPicker = pattern<EmojiPickerInput, EmojiPickerInput>(
  ({ selectedEmoji }) => {
    const displayText = computed(() =>
      selectedEmoji ? `Selected: ${selectedEmoji}` : "None"
    );

    return {
      [NAME]: computed(() => `${MODULE_METADATA.icon} Emoji: ${displayText}`),
      [UI]: (
        <ct-hstack style={{ gap: "8px", alignItems: "center" }}>
          <div
            style={{
              fontSize: "32px",
              lineHeight: "1",
              minWidth: "40px",
              textAlign: "center",
            }}
          >
            {selectedEmoji || "\u{2754}"}
          </div>
          <ct-autocomplete
            items={EMOJI_ITEMS}
            placeholder="Search emojis..."
            onct-select={onSelectEmoji({ selectedEmoji })}
            style={{ flex: "1" }}
          />
          <ct-button onClick={clearSelection({ selectedEmoji })}>
            Clear
          </ct-button>
        </ct-hstack>
      ),
      selectedEmoji,
    };
  },
);

export default EmojiPicker;
